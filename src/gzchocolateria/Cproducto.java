    private void guardarP(){
        String Id_producto =txtIdP.getText();
        String nombre_producto =txtNombreP.getText();
        String cantidad  =txtCantidadP.getText();
        String precio =txtPrecioP.getText();
        String descripcion =txtDescripcion.getText();
     
        if (nombre_producto.isEmpty() || cantidad.isEmpty() || precio.isEmpty() || descripcion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos obligatorios.");
        return;
    }
        try (Connection conn = dbConnection.getConnection()) {
            String sql = "INSERT INTO inventario (Id_producto, nombre_producto, cantidad, precio, Descripcion) "
               + "VALUES (?, ?, ?, ?, ?)";

        try (PreparedStatement as = conn.prepareStatement(sql)) {
        as.setString(1, Id_producto);
        as.setString(2, nombre_producto);
        as.setString(3, cantidad);
        as.setString(4, precio);
        as.setString(5, descripcion); 
          int rowsAffected = as.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Producto registrado exitosamente.");
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar producto.");
        }
    }
} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Error de conexi√≥n a la base de datos: " + e.getMessage());
}
    }

