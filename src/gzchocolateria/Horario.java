    private void btnAsignarHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarHActionPerformed
        String trabajadorNombre = cmbTrabajador.getSelectedItem().toString();
    java.util.Date fecha = HorarioF.getDate();
    String horaEntrada = txtHoraE.getText();
    String horaSalida = txtHoraS.getText();

    if (fecha == null || horaEntrada.isEmpty() || horaSalida.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
        return;
    }

    try (Connection conn = dbConnection.getConnection()) {
        // Obtener el Id_trabajador usando el nombre seleccionado
        String idTrabajador;
            idTrabajador = "";
        try (PreparedStatement getIdStmt = conn.prepareStatement(
                "SELECT Id_trabajador FROM datos_trabajador WHERE nombre = ?")) {
            getIdStmt.setString(1, trabajadorNombre);
            ResultSet rs = getIdStmt.executeQuery();
            if (rs.next()) {
                idTrabajador = rs.getString("Id_trabajador");
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró el trabajador seleccionado.");
                return;
            }
        }

        // Insertar el horario
        java.sql.Date sqlFecha = new java.sql.Date(fecha.getTime());
        try (PreparedStatement insertStmt = conn.prepareStatement(
                "INSERT INTO Horario (Id_trabajador, fecha, hora_entrada, hora_salida) VALUES (?, ?, ?, ?)")) {
            insertStmt.setString(1, idTrabajador);
            insertStmt.setDate(2, sqlFecha);
            insertStmt.setString(3, horaEntrada);
            insertStmt.setString(4, horaSalida);
            insertStmt.executeUpdate();
        }

        JOptionPane.showMessageDialog(this, "Horario asignado correctamente.");
        limpiarCampos();
        cargarHorarios(); // Refrescar la tabla
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al asignar horario: " + e.getMessage());
    }
}

private void limpiarCampos() {
    HorarioF.setDate(null);
    txtHoraE.setText("");
    txtHoraS.setText("");

    }//GEN-LAST:event_btnAsignarHActionPerformed

