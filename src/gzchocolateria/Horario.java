    private void cargarTrabajadores() {
    try (Connection conn = dbConnection.getConnection();
         PreparedStatement stmt = conn.prepareStatement("SELECT nombre FROM datos_trabajador");
         ResultSet rs = stmt.executeQuery()) {

        cmbTrabajador.removeAllItems(); // Limpiar ComboBox
        while (rs.next()) {
            cmbTrabajador.addItem(rs.getString("nombre"));
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar trabajadores: " + e.getMessage());
    }
}
    
    private void cargarHorarios() {
        // Consulta SQL
            String query = "SELECT horario.Id_horario, horario.Id_trabajador, datos_trabajador.Nombre AS Nombre_Trabajador, " +
                           "horario.hora_entrada, horario.hora_salida, horario.Fecha " +
                           "FROM horario " +
                           "INNER JOIN datos_trabajador ON horario.Id_trabajador = datos_trabajador.Id_trabajador";

            // Ejecutar consulta
            
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("Id_trabajador");
    modelo.addColumn("Nombre_Trabajador");
    modelo.addColumn("Fecha");
    modelo.addColumn("Hora de Entrada");
    modelo.addColumn("Hora de Salida");

    try (Connection conn = dbConnection.getConnection();
         PreparedStatement stmt = conn.prepareStatement(query);
         ResultSet rs = stmt.executeQuery()) {

        while (rs.next()) {
            Object[] fila = {
                
                rs.getString("Id_trabajador"),
                rs.getString("Nombre_Trabajador"),
                rs.getDate("fecha"),
                rs.getString("hora_entrada"),
                rs.getString("hora_salida")
            };
            modelo.addRow(fila);
        }
        TablaHorario.setModel(modelo);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar horarios: " + e.getMessage());
    }
}
    



